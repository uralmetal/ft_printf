#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list *rabotaga;

	if (!lst || !f)
		return ;
	rabotaga = lst;
	while (rabotaga->next != NULL)
	{
		f(rabotaga);
		rabotaga = rabotaga->next;
	}
	f(rabotaga);
	rabotaga = rabotaga->next;
}
